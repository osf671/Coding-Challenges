function setupAndRun(func) {
  const tests = [
    {
      input: "ppoeemm",
      expectation: "poem"
    },
    {
      input: "wiiiinnnnd",
      expectation: "wind"
    },
    {
      input: "ttiiitllleeee",
      expectation: "title"
    },
    {
      input: "cccccaaarrrbbonnnnn",
      expectation: "carbon"
    }
  ];

  return tests.map(({ input, expectation }) => {
    const functionReturn = func(input);
    const passed = functionReturn === expectation;
    const message =
      input + (passed ? ": THE TEST PASSED" : ": THE TEST FAILED");
    return {
      input,
      expectation,
      functionReturn,
      passed,
      message
    };
  });
}
