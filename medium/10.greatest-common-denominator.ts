function setupAndRun(func) {
  const tests = [
    {
      input: [32, 8],
      expectation: 8
    },
    {
      input: [8, 12],
      expectation: 4
    },
    {
      input: [17, 13],
      expectation: 1
    }
  ];

  return tests.map(({ input, expectation }) => {
    const functionReturn = func(input[0], input[1]);
    const passed = functionReturn === expectation;
    const message =
      input + (passed ? ": THE TEST PASSED" : ": THE TEST FAILED");
    return {
      input,
      expectation,
      functionReturn,
      passed,
      message
    };
  });
}
