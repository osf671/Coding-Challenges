function setupAndRun(func) {
  const tests = [
    {
      input: "loop",
      expectation: true
    },
    {
      input: "yummy",
      expectation: true
    },
    {
      input: "orange",
      expectation: false
    },
    {
      input: "munchkin",
      expectation: false
    }
  ];

  return tests.map(({ input, expectation }) => {
    const functionReturn = func(input);
    const passed = functionReturn === expectation;
    const message =
      input + (passed ? ": THE TEST PASSED" : ": THE TEST FAILED");
    return {
      input,
      expectation,
      functionReturn,
      passed,
      message
    };
  });
}
