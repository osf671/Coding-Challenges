function doubleLetters(word) {
  for (let i = 0; i < word.length; i++) {
    let currentChar = word[i];
    let nextChar = word[i+1];
    if (i + 1 < word.length && currentChar === nextChar) {
      return true;
    }
  }
  return false;
}
