function setupAndRun(func) {
  const tests = [
    // {
    //   input: [[1], [2], [3], [4]],
    //   expectation: 4
    // },
    // {
    //   input: [[1, 2], [2, 3], [3, 4], [4, 4]],
    //   expectation: 1
    // },
    {
      input: [[33, 33], [5], ["a", "a"], [2, 2, 2], [1, 2, 2], [3, 1]],
      expectation: 4
    },
    // {
    //   input: [["@", "@", "@", "@"], [2, 3], [3, 4], [4, 4]],
    //   expectation: 2
    // }
  ];

  return tests.map(({ input, expectation }) => {
    const functionReturn = func(input);
    const passed = functionReturn === expectation;
    const message =
      input + (passed ? ": THE TEST PASSED" : ": THE TEST FAILED");
    return {
      input,
      expectation,
      functionReturn,
      passed,
      message
    };
  });
}
