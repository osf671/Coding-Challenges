function identicalSubArrays(arr) {

  var totalIdenticalSubArr = 0;


  for (var x = 0; x < arr.length; x++) {
    var curSubArr = arr[x];
    var newSet= new Set(curSubArr)
    if ([...newSet.values].length===1){
      totalIdenticalSubArr += 1;
    }
  }
  return totalIdenticalSubArr;
}
