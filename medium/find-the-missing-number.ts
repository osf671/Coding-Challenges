function setupAndRun(func) {
  const tests = [
    {
      input: [1, 2, 3, 4, 6, 7, 8, 9, 10],
      expectation: 5
    },
    {
      input: [7, 2, 3, 6, 5, 9, 1, 4, 8],
      expectation: 10
    },
    {
      input: [10, 5, 1, 2, 4, 6, 8, 3, 9],
      expectation: 7
    }
  ];

  return tests.map(({ input, expectation }) => {
    const functionReturn = func(input);
    const passed = functionReturn === expectation;
    const message =
      input + (passed ? ": THE TEST PASSED" : ": THE TEST FAILED");
    return {
      input,
      expectation,
      functionReturn,
      passed,
      message
    };
  });
}
