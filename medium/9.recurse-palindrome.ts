function isPalindrome(str) {
  if (str.length < 2) {
    return true;
  }
  let firstChar = str[0];
  let lastChar = str[str.length-1];
  if (firstChar === lastChar) {
    return isPalindrome(str.slice(1, str.length-1))
  }
  return false;
}
