function isPalindrome(str) {
  var lowCaseStr = str.toLowerCase();
  var letterArr= lowCaseStr.split("");
  var reverseArr= letterArr.reverse();
  var finalStr= reverseArr.join("")

  return lowCaseStr == finalStr;



}
